/* There are more data types than this, but these are the most common */
/* characters only */
char
/* with nvarchar it's a string of text and you can specify how many character each column holds */
nvarchar(max)
/* varchar */
varchar(20)
/* as the name suggests stores binary */
binary(max)
varbinary(max)
/* stores a boolean result, of 0 or 1, or NULL */
bit
/* integar value */
int
/* floating point integer */
float(max)
/* store a date value */
datetime2
/* unique identifier */
uniqueidentifier